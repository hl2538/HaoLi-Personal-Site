package com.haoli.domain;

public class Text {
	
	
	public String from;
	public String to;
	public String contentBeforeTranslated;
	public String contentAfterTranslated;
	
	public void setFrom(String from) {
		this.from = from;
	}
	
	public void setTo(String to) {
		this.to = to;
	}
	
	public void setContentBeforeTranslated(String contentBeforeTranslated) {
		this.contentBeforeTranslated = contentBeforeTranslated;
	}
	
	public void setContentAfterTranslated(String contentAfterTranslated) {
		this.contentAfterTranslated = contentAfterTranslated;
	}
	
}
